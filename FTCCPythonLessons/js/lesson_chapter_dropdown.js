
 
function chapterOneLessonDropdown() {
        var chapter1Div = document.getElementById("chapter-1-div");
if (chapter1Div.style.display === "block") {
    chapter1Div.style.display = "none";
        } else {
    chapter1Div.style.display = "block";
chapter1Div.style.width = "100%"; // Adjust the width as needed
        }
    }

