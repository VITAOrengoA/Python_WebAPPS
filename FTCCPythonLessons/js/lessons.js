function openLessonOne() {
    document.getElementById("lesson-1-div").style.height = "800px";
    document.getElementById("lesson-1-btn-close").style.display = "block";
    document.getElementById("lesson-1-btn-open").style.display = "none";
}

function closeLessonOne() {
    document.getElementById("lesson-1-div").style.height = "50px";
    document.getElementById("lesson-1-btn-close").style.display = "none";
    document.getElementById("lesson-1-btn-open").style.display = "block";
}

